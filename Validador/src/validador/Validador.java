public class Validador {
     public Validador(){  
    } 
    public boolean menorQue(int a, int b){
        return a < b;
    }
    
    public boolean mayorQue(int a, int b){
        return a > b;
    }
    
    public boolean igualQue(int a, int b){
        return a==b;
    }
}
